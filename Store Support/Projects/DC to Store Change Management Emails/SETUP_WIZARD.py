#!/usr/bin/env python3
"""
ELM Manager Change Detection - Setup Wizard
Interactive configuration setup for new deployments
"""

import os
import sys
from pathlib import Path

def print_header(text):
    """Print a formatted header"""
    print("\n" + "="*70)
    print(text)
    print("="*70 + "\n")

def get_input(prompt, default=None):
    """Get user input with optional default"""
    if default:
        full_prompt = f"{prompt} [{default}]: "
    else:
        full_prompt = f"{prompt}: "
    
    value = input(full_prompt).strip()
    return value if value else default

def yes_no(prompt, default=True):
    """Get yes/no input"""
    default_str = "Y/n" if default else "y/N"
    response = input(f"{prompt} [{default_str}]: ").strip().lower()
    
    if not response:
        return default
    return response in ['y', 'yes']

def main():
    print_header("ELM MANAGER CHANGE DETECTION - SETUP WIZARD")
    
    print("This wizard will help you configure the system for your environment.")
    print("You can press CTRL+C at any time to cancel.\n")
    
    input("Press ENTER to begin...")
    
    # Step 1: User Information
    print_header("STEP 1: User Information")
    
    user_email = get_input(
        "Your Walmart email address",
        os.environ.get('USERNAME', '') + "@homeoffice.wal-mart.com"
    )
    
    user_name = get_input(
        "Your name (for notifications)",
        os.environ.get('USERNAME', 'Admin')
    )
    
