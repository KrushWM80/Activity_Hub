    print("You need 'Send As' permissions for the shared mailbox.\n")
    
    shared_mailbox = get_input(
        "Shared mailbox email address",
        "supplychainops@email.wal-mart.com"
    )
    
    shared_mailbox_name = get_input(
        "Shared mailbox display name",
        "Supply Chain Integration Operations"
    )
    
    print("\nIMPORTANT: Make sure you have added this shared mailbox to Outlook:")
    print("  1. Open Outlook")
    print("  2. File -> Account Settings -> Account Settings")
    print("  3. Select your account -> Change -> More Settings -> Advanced")
    print(f"  4. Add: {shared_mailbox}")
    print()
    
    has_mailbox = yes_no("Have you added the shared mailbox to Outlook?", False)
    if not has_mailbox:
        print("\n[WARNING] Please add the shared mailbox to Outlook before continuing.")
        print("The system will not be able to send emails without it.\n")
        if not yes_no("Continue anyway?", False):
            print("\nSetup cancelled. Please add the shared mailbox and run this wizard again.")
            return 1
    
