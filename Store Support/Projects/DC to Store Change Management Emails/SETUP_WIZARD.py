    print_header("STEP 3: Test Mode Configuration")
    
    print("Test mode sends ALL emails to you instead of production distribution lists.")
    print("This is recommended for initial setup and testing.\n")
    
    test_mode = yes_no("Enable test mode?", True)
    
    if test_mode:
        print(f"\n[OK] Test mode enabled. All emails will go to: {user_email}")
    else:
        print("\n[WARNING] Test mode disabled. Emails will go to production DCs.")
        if not yes_no("Are you sure?", False):
            test_mode = True
            print("[OK] Test mode re-enabled for safety.")
    
    # Step 4: Location Filters
    print_header("STEP 4: Location Filters")
    
    print("By default, the system tracks:")
    print("  - Walmart Supercenters, Neighborhood Markets, Discount Stores")
    print("  - Sam's Club locations")
    print("  - ~5,200 total locations")
    print()
    
    use_filters = yes_no("Use default location filters?", True)
    
    # Step 5: VPN Retry Configuration
    print_header("STEP 5: VPN Retry Configuration")
    
    print("The system can retry for up to 7 days if VPN is unavailable.")
    print("Task Scheduler will run hourly and keep trying.\n")
    
    vpn_retry_days = int(get_input("Maximum retry days", "7"))
    
    # Step 6: Task Scheduler
    print_header("STEP 6: Task Scheduler Configuration")
    
    print("The system uses Windows Task Scheduler to run automatically.")
    print("Recommended: Run hourly starting at 2:00 AM\n")
    
    start_time = get_input("Start time (HH:MM format)", "02:00")
    
    setup_task = yes_no("Set up Task Scheduler now?", True)
    
